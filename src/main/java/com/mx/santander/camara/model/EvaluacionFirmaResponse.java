package com.mx.santander.camara.model;

public class EvaluacionFirmaResponse {
	
	private String porcentaje;
	private String numFirmas;
	private String evaluacionCheque;

	public String getPorcentaje() {
		return porcentaje;
	}

	public void setPorcentaje(String porcentaje) {
		this.porcentaje = porcentaje;
	}

	public String getNumFirmas() {
		return numFirmas;
	}

	public void setNumFirmas(String numFirmas) {
		this.numFirmas = numFirmas;
	}

	public String getEvaluacionCheque() {
		return evaluacionCheque;
	}

	public void setEvaluacionCheque(String evaluacionCheque) {
		this.evaluacionCheque = evaluacionCheque;
	}

	

}